/*
    Round Syntax: Math.round(x);

    Rounding To The Nearest Integer:
    0.00 => 0.49 ==> 0
    0.50 => 0.99 ==> 1
*/

var x = Math.round(4.2);
console.log(x); // 4

var y = Math.round(4.7);
console.log(y); // 5

var z = Math.round(-4.2);
console.log(z); // -4

var w = Math.round(-4.7);
console.log(w); // -5

