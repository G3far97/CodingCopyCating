/*
    Ceil Syntax: Math.ceil(x); // Rounding Up To The Nearest Integer
*/

var x = Math.ceil(4.2);
console.log(x); // 5

var y = Math.ceil(-4.2);
console.log(y); // -4 // Because '-4' is Bigger Than '-4.2'

