/*
    Floor Syntax: Math.floor(x); // Rounding Down To The Nearest Integer
*/

var x = Math.floor(4.2);
console.log(x); // 4

var y = Math.floor(-4.2);
console.log(y); // -5 // Because '-5' is Smaller Than '-4.2'

