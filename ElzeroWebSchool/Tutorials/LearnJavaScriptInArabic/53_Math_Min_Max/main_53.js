/*
    Min Syntax: Math.min(x1, x2, x3, ..., xn); // Finds The Minimum Value
    Max Syntax: Math.max(x1, x2, x3, ..., xn); // Finds The Maximum Value
*/

var x = Math.min(100, 10, 50, 1000.50, -90, -120, -100.50, 150.10);
console.log(x); // -120

var y = Math.max(100, 10, 50, 1000.50, -90, -120, -100.50, 150.10);
console.log(Math.floor(y)); // 1000

var z = Math.max(100, 10, 50, 1000.50, -90, -120, -100.50, 150.10);
console.log(Math.round(z)); // 1001

